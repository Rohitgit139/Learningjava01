package Wrapper_class;

public class Boxing_and_Unboxing {

    public static void main(String[]args){
        int  a=10;
        Integer b=a;
        System.out.println(a);
        System.out.println(b);




        //unboxing

        Integer a1=10;
        int b1=a1;
        System.out.println(a1);
        System.out.println(b1);
    }
}
